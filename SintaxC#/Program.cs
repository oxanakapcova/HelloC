Console.WriteLine(true); //логический литерал Правда
Console.WriteLine(false); //логический литерал Ложь
// литерал это как константа неизменяемое
//null отсутствие значения
Console.WriteLine(-11); //целые десятичные числа
Console.WriteLine(5);
Console.WriteLine(505);
Console.WriteLine(0b11); //числа в двоичной системе вначале пишем 0b
